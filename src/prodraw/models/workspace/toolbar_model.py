TOOLBAR_BG = "#303035"
TOOLBAR_WIDTH = 300
TOOLBAR_HEIGHT = 50
BUTTON_SIZE = 50
BUTTON_PADDING = 12


class ToolbarModel:
    """Holds toolbar layout and style configuration."""

    def __init__(self):
        self.background = TOOLBAR_BG
        self.width = TOOLBAR_WIDTH
        self.height = TOOLBAR_HEIGHT
        self.button_size = BUTTON_SIZE
        self.button_padding = BUTTON_PADDING
